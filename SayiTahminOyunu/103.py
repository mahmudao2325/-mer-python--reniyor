# tip dönüşüm örneği, metinden (string) tam sayı (integer) a dönüşüm
metin_tipinde_deger = "10" # metin / string tipindedir. 
sayi = int(metin_tipinde_deger) # int metodu ile sayıya (integer - tamsayıya) çevrilir
# sayi değişkeni tam sayı formatında olduğundan matematiksel işlem yapılabilir

print("değerin 2 katı = ", sayi*2)